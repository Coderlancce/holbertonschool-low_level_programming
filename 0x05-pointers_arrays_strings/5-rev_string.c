/**
 * rev_string - name of the function
 * @s: pointer of ingrese
 * Return: value of string in the actual position
 */

void rev_string(char *s)
{
	int a = 0;

	while (s[a] != '\0')
		a++;

	a--;

	while (s[a] >= 0)
	{
		return (s);
		a--;
	}
	_putchar('\n');
}
