#include <stdio.h>
#include "main.h"

/**
 * print_triangle - name of the function
 * @size: value of ingrese
 * Return: none
 */

void print_triangle(int size)
{
	int x, y = 0;
	int space;

	if (size > 0)
	{
		while (y < size)
		{
			space = size - y - 1;
			while (x < size)
			{
				if (space > x)
				{
					putchar(' ');
				}
				else
				{
					_putchar('#');
				}
				x++;
			}
			y++;
			x = 0;
			_putchar ('\n');
		}
	}
	else
	_putchar('\n');
}
